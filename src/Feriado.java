public class Feriado {
    private String data;
    private String feriado;

    public Feriado(String data, String feriado) {
        this.data = data;
        this.feriado = feriado;
    }
    
    public String getData() {
        return this.data;
    }

    public String getFeriado() {
        return this.feriado;
    }
}
